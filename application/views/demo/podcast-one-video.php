
<h1>Study explained</h1>

<p>Studying with The Open University can be an inspiring, challenging and ultimately rewarding experience. Yet certain 'facts of life' inevitably get in the way and questions can arise. Can I afford it? Do I have the time? Is it worth it? This site is designed to give you the answers to some of those questions.

<p>We can't change the realities of work and family life, but we can show you how the OU can make it easier for students to discover and develop their true potential.

